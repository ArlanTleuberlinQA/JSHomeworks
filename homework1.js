console.log('number' + 3 + 3); // 'number' є рядком. 3 конвертується в рядок і додається до 'number'. Отримуємо 'number3'. Далі 3 знову конвертується в рядок і додається до 'number3'. Результат: 'number33'

console.log(null + 3); // null конвертується в 0. Отримуємо 0 + 3. Результат: 3

console.log(5 && "qwerty"); // 5 є істинним значенням. Переходимо до "qwerty", яке також є істинним значенням. Результат: "qwerty"

console.log(+'40' + +'2' + "hillel"); // Унарний плюс конвертує '40' в число 40 і '2' в число 2. 40 + 2 дорівнює 42. Додаємо "hillel", 42 конвертується в рядок. Результат: '42hillel'

console.log('10' - 5 === 6); // '10' конвертується в число 10. 10 - 5 дорівнює 5. 5 === 6 - порівняння типів і значень. 5 не дорівнює 6. Результат: false

console.log(true + false); // true конвертується в 1, false конвертується в 0. 1 + 0 дорівнює 1. Результат: 1

console.log('4px' - 3); // '4px' не може бути конвертований в число. Віднімання неможливо з рядком. Результат: NaN

console.log('4' - 3); // '4' конвертується в число 4. 4 - 3 дорівнює 1. Результат: 1

console.log('6' + 3 ** 0); // 3 ** 0 дорівнює 1. '6' є рядком. Рядок '6' додається до числа 1, яке конвертується в рядок. Результат: '61'

console.log(12 / '6'); // '6' конвертується в число 6. 12 / 6 дорівнює 2. Результат: 2

console.log('10' + (5 === 6)); // 5 === 6 дорівнює false. '10' є рядком. Рядок '10' додається до false, який конвертується в рядок 'false'. Результат: '10false'

console.log(null == ''); // null не є рівним '' за порівнянням. Результат: false

console.log(3 ** (9 / 3)); // 9 / 3 дорівнює 3. 3 ** 3 дорівнює 27. Результат: 27

console.log(!!'false' == !!'true'); // Рядки 'false' та 'true' обидва є істинними значеннями, оскільки будь-який непорожній рядок є істиним значенням. !!'false' та !!'true' обидва дорівнюють true. true == true дорівнює true. Результат: true

console.log(0 || '0' && 1); // спочатку виконується &&(І): '0' && 1 повертає 1 (останнє істинне значення). Потім виконується ||(АБО): 0 || 1 повертає 1 (перше істинне значення).

console.log((+null == false) < 1); // +null дорівнює 0. false конвертується в 0. 0 == 0 дорівнює true. true конвертується в 1. 1 < 1 дорівнює false. Результат: false

console.log(false && true || true); // false && true дорівнює false. false || true дорівнює true. Результат: true

console.log(false && (false || true)); // false || true дорівнює true. false && true дорівнює false. Результат: false

console.log((+null == false) < 1 ** 5); // +null дорівнює 0. false конвертується в 0. 0 == 0 дорівнює true. 1 ** 5 дорівнює 1. true конвертується в 1. 1 < 1 дорівнює false. Результат: false